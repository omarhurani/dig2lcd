    private void createCode(){
        String str, s;
        int index;
        addBegin();
        
        for(int i = 0; i < listModel.getSize(); i++){
            str = (String)listModel.get(i);
            index = Integer.valueOf(str.substring(0, 2).trim());
            switch(index){
                case 1:
                    addInitialize();
                    break;
                case 2:
                    addConfigure();
                    break;
                case 3:
                    addCommand(false, false, "00000001", "Clear Display");
                    break;
                case 4:
                    addCommand(false, false, "00000010", "Return Cursor Home");
                    break;
                case 5:
                    s = str.substring(3);
                    addCommand(false, false, "000001"+s, String.format("Entry Mode Set (D/I=%c, S=%c)",s.charAt(0), s.charAt(1)));
                    break;
                case 6:
                    s = str.substring(3);
                    addCommand(false, false, "00001"+s, String.format("Display On/ Off (D=%c, C=%c, B=%c)",s.charAt(0), s.charAt(1), s.charAt(2)));
                    break;
                case 7:
                    s = str.substring(3);
                    addCommand(false, false, "0001"+s+"00", String.format("Cursor and Display Shift (S/C=%c, R/L=%c)",s.charAt(0), s.charAt(1)));
                    break;
                case 8:
                    addCommand(false, false, "00101000", "Function Set");
                    break;
                case 9:
                    s = str.substring(4);
                    addCommand(false, false, "01"+s, "Set CG RAM Address = "+s);
                    break;
                case 10:
                    s = str.substring(5);
                    addCommand(false, false, "1"+s, "Set DD RAM Address = "+s);
                    break;
                case 11:
                    break;
                case 12:
                    s = str.substring(5);
                    if(s.length()==8){
                        addCommand(true, false, s, "Write Data to CG RAM or DD RAM: "+s);
                    }
                    else{
                        addCommand(true, false, decodeCharacter(s.charAt(0)), "Write Data to CG RAM or DD RAM: "+s+" ["+decodeCharacter(s.charAt(0))+"]");
                    }
                    break;     
                case 13:
                    break;
                case 14:
                    s = str.substring(4).trim();
                    addDelay(Integer.parseInt(s));
                    break;
            }
        }
        addEndIf();
        addEnd();
        writeFile(this);
    }
