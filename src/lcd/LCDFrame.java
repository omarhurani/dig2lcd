    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        StringBuilder toAdd = new StringBuilder("");
        toAdd.append(commandComboBox.getSelectedIndex()+1);
        String str;
        switch(commandComboBox.getSelectedIndex()){
            case 0: toAdd.append(" "); break;
            case 1: toAdd.append(" "); break;
            case 2: toAdd.append(" "); break;
            case 3: toAdd.append(" "); break;
            case 4:
                toAdd.append(" :");
                toAdd.append(jComboBoxDI.getSelectedIndex());
                toAdd.append(jComboBoxS.getSelectedIndex());
                break;
            case 5:
                toAdd.append(" :");
                toAdd.append(jComboBoxD.getSelectedIndex());
                toAdd.append(jComboBoxC.getSelectedIndex());
                toAdd.append(jComboBoxB.getSelectedIndex());
                break;
            case 6:
                toAdd.append(" :");
                toAdd.append(jComboBoxSC.getSelectedIndex());
                toAdd.append(jComboBoxRL.getSelectedIndex());
                break;
            case 7: toAdd.append(" "); break;
            case 8:
                str = jTextFieldCG.getText().trim();
                if(str.length() != 6){
                    JOptionPane.showMessageDialog(this, "Input should be 6 bits long!", "Ooops!", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                for(int i = 0 ; i < str.length(); i++){
                    if(str.charAt(i) != '1' && str.charAt(i) != '0'){
                        JOptionPane.showMessageDialog(this, "Input should be binary!", "Ooops!", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                }
                toAdd.append(" : ");
                toAdd.append(str);
                break;
            case 9:
                str = jTextFieldDD.getText().trim();
                if(str.length() != 7){
                    JOptionPane.showMessageDialog(this, "Input should be 7 bits long!", "Ooops!", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                for(int i = 0 ; i < str.length(); i++){
                    if(str.charAt(i) != '1' && str.charAt(i) != '0'){
                        JOptionPane.showMessageDialog(this, "Input should be binary!", "Ooops!", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                }
                toAdd.append(" : ");
                toAdd.append(str);
                break;
            case 10:
                JOptionPane.showMessageDialog(this, "Not implemented!", "Ooops!", JOptionPane.ERROR_MESSAGE);
                return;
            case 11:
                str = jTextFieldWrite.getText().trim();
                if(jComboBoxWrite.getSelectedIndex()==0 && str.length() != 1){
                    JOptionPane.showMessageDialog(this, "Input should be 1 character only!", "Ooops!", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                else if(jComboBoxWrite.getSelectedIndex()==1){
                    if(str.length() != 8){
                        JOptionPane.showMessageDialog(this, "Input should be 8 bits long!", "Ooops!", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                    for(int i = 0 ; i < str.length(); i++){
                    if(str.charAt(i) != '1' && str.charAt(i) != '0'){
                        JOptionPane.showMessageDialog(this, "Input should be binary!", "Ooops!", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                }
                    
                }
                toAdd.append(" : ");
                toAdd.append(str);
                break;
            case 12:
                JOptionPane.showMessageDialog(this, "Not implemented!", "Ooops!", JOptionPane.ERROR_MESSAGE);
                return;
            case 13:
                toAdd.append(" : ");
                try{
                    int cycles = Integer.parseInt(jTextFieldCycles.getText().trim());
                    if(cycles <= 0)
                        throw new Exception();
                    toAdd.append(String.valueOf(cycles));
                } catch (Exception e){
                    JOptionPane.showMessageDialog(this, "Cycles should be a positive number!", "Ooops!", JOptionPane.ERROR_MESSAGE);
                }
                break;
            
        }
        listModel.addElement(toAdd.toString());
    }//GEN-LAST:event_addButtonActionPerformed

